
public class Demo2 {
    public static void main(String[] args) {
     /* Typecasting */
     /* Implicit casting  */
     int a = 34;
     long b = a; // Automatic casting
     System.out.println(b);
    /* Explicit Casting */
     int f = 56;
     short d = (short)f;//Manual Casting
     System.out.println(d);
    }
}